local servers = { "lemminx", "clangd", "rust_analyzer", "elixirls", "texlab", "hls", "solargraph" }

for _, lsp in ipairs(servers) do
  lspconfig[lsp].setup {
    on_attach = custom_on_attach,
    capabilities = capabilities,
  }
end

return M

